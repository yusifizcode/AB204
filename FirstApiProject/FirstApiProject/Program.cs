builder.Services.AddSwaggerGen();

builder.Services.AddDbContext<AppDbContext>(opt =>
{
    opt.UseSqlServer("Server=DESKTOP-L6JGBQE\\SQLEXPRESS;Database=AB204FirstAPIDB;Trusted_Connection=true;TrustServerCertificate=true;Integrated Security=true;");
});

var app = builder.Build();

// Configure the HTTP request pipeline.
if (app.Environment.IsDevelopment())
{
    app.UseSwagger();
